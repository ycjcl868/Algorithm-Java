import java.math.BigDecimal;
// 有效数字100位，任意精度小数
public class 浮点 {
    public static void main(String[] args){



        double a = 3.0/0;
        System.out.println(a+1);
        System.out.println(a+a);
        System.out.println(1/a);
        System.out.println(a*-1);

        double b = 0.0/0.0;
        System.out.println(b);
        System.out.println(b+12);


    }
}
